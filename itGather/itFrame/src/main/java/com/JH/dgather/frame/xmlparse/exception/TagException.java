package com.JH.dgather.frame.xmlparse.exception;

public class TagException extends Exception {
	/**
	 * 
	 */
	private static final long serialVersionUID = -3999289677912068983L;

	public TagException() {
		super();
	}
	
	public TagException(String message) {
		super(message);
	}
	
	public TagException(String message, Throwable cause) {
		super(message, cause);
	}
	
	public TagException(Throwable cause) {
		super(cause);
	}
}
