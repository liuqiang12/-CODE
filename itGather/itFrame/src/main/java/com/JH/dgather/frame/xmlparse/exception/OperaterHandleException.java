package com.JH.dgather.frame.xmlparse.exception;

public class OperaterHandleException extends Exception {
	/**
	 * 
	 */
	private static final long serialVersionUID = 4260144181320512297L;
	
	public OperaterHandleException() {
		super();
	}
	
	public OperaterHandleException(String message) {
		super(message);
	}
	
	public OperaterHandleException(String message, Throwable cause) {
		super(message, cause);
	}
	
	public OperaterHandleException(Throwable cause) {
		super(cause);
	}
}
